# 1. డేటాను మోడల్కి అందించే డెలివరీ బాయ్ (DataLoader)
# ─────────────────────────────────────────────────────────────────
# బేసిక్: మీ దగ్గర 10,000 ఫోటోలు ఉంటే, వాటన్నింటినీ ఒకేసారి కంప్యూటర్ మెమరీ (RAM)
# లోకి ఎక్కించలేము, సిస్టమ్ హ్యాంగ్ అవుతుంది.
#
# ఇక్కడ ఏం జరుగుతోంది?: DataLoader అనేది ఒక డెలివరీ బాయ్ లాంటిది.
# ఇది మీ హార్డ్ డిస్క్ లో ఉన్న 10,000 ఫోటోలలో నుండి,
# ఒకేసారి ఒక ఫోటోను (batch_size=1) తీసి AI మోడల్ కి ఇస్తుంది.
# ─────────────────────────────────────────────────────────────────
from src.data.components.KITTI_dataset import KITTI
import torch
from src.utils import custom_transform

transform_train = [custom_transform.ToTensor(),
                   custom_transform.Resize((256, 512))]
transform_train = custom_transform.Compose(transform_train)
dataset = KITTI("kitti_data", train_seqs=['00','01','02','04','06', '08', '09'], transform=transform_train, sequence_length=11)
save_dir = "kitti_latent_data/train_10"
loader = torch.utils.data.DataLoader(dataset, batch_size=1, shuffle=True)

# ─────────────────────────────────────────────────────────────────
# 2. మోడల్ కి కావలసిన సెట్టింగ్స్ (Parameters)
# ─────────────────────────────────────────────────────────────────
# బేసిక్: ఏ మోడల్ కి అయినా మనం ఇస్తున్న ఫోటో సైజు ఎంత,
# మనకు బయటకి కావలసిన అవుట్పుట్ సైజు ఎంత అని ముందే చెప్పాలి.
#
# ఇక్కడ ఏం జరుగుతోంది?: ఫోటో వెడల్పు 512, ఎత్తు 256 అని సెట్ చేశారు.
# మోడల్ బయటకి ఇచ్చే జ్యూస్ (ఫీచర్స్) పొడవు → visual: 512, IMU: 256 నంబర్స్ ఉండాలి.
#
# Encoder కి `opt` అనే object కావాలి (plain dictionary కాదు) — `opt.img_w` అని access చేయాలి.
# SimpleNamespace అనేది Python built-in, దీనివల్ల class రాయాల్సిన పనిలేదు.
# SimpleNamespace(**params) అంటే dictionary లోని అన్ని keys ని dot attributes గా మారుస్తుంది.
# ─────────────────────────────────────────────────────────────────
from types import SimpleNamespace
from src.models.components.vsvio import Encoder
params = {"img_w": 512, "img_h": 256, "v_f_len": 512, "i_f_len": 256, "imu_dropout": 0.1, "seq_len": 11}
params = SimpleNamespace(**params)

# ─────────────────────────────────────────────────────────────────
# 3. ముందే చదువుకున్న బ్రెయిన్ను లోడ్ చేయడం (Pre-trained Weights)
# ─────────────────────────────────────────────────────────────────
# బేసిక్: ఒక కొత్త న్యూరల్ నెట్వర్క్ను సృష్టించినప్పుడు దానికి ఏమీ తెలియదు
# (అప్పుడే పుట్టిన పిల్లాడిలాగా). దానికి డేటా ఇచ్చి ట్రైన్ చేస్తే,
# అది కొన్ని నంబర్లను (Weights) తన బ్రెయిన్ లో స్టోర్ చేసుకుంటుంది.
#
# ఇక్కడ ఏం జరుగుతోంది?: మనం FeatureEncodingModel ని కొత్తగా క్రియేట్ చేశాం.
# కానీ దానికి మళ్ళీ ట్రైనింగ్ ఇచ్చే సమయం మనకు లేదు. అందుకే గతంలో ఎప్పుడో
# ట్రైన్ చేసి, ఫైల్ లాగా సేవ్ చేసిన బ్రెయిన్ (జ్ఞానం) pretrained_w ని తెచ్చి
# మన కొత్త మోడల్ లోకి ఎక్కిస్తున్నాం (load_state_dict).
# ─────────────────────────────────────────────────────────────────
# ─────────────────────────────────────────────────────────────────

class FeatureEncodingModel(torch.nn.Module):
    def __init__(self, params):
        super(FeatureEncodingModel, self).__init__()
        self.Feature_net = Encoder(params)
    def forward(self, imgs, imus):
        feat_v, feat_i = self.Feature_net(imgs, imus)
        return feat_v, feat_i
    
model = FeatureEncodingModel(params)

pretrained_w = torch.load("../pretrained_models/vf_512_if_256_3e-05.model", map_location='cpu')
model_dict = model.state_dict()
# torch.nn.Module అనేది PyTorch లోని మోడల్స్ అన్నింటికీ "తల్లి (Parent Class)" లాంటిది.
# మీరు మీ క్లాస్ ను క్రియేట్ చేసేటప్పుడు బ్రాకెట్లలో torch.nn.Module ని పెట్టారు
# (దీన్నే ప్రోగ్రామింగ్లో Inheritance అంటారు).
# దానివల్ల ఆ పేరెంట్ క్లాస్లో ఉన్న ఫీచర్స్ (Superpowers) అన్నీ
# ఆటోమెటిక్గా మీ కొత్త మోడల్కి వచ్చేశాయి. ఆ ఫీచర్స్లో ఒకటే ఈ .state_dict().
# ─────────────────────────────────────────────────────────────────
# అసలు state_dict() అంటే ఏంటి? అది ఏం ఇస్తుంది?
# దీని పేరులోనే అర్థం ఉంది: State + Dictionary.
# State (స్థితి): మోడల్ లోపల ఉన్న లేయర్స్, అవి నేర్చుకున్న వెయిట్స్ (నంబర్లు).
# Dict (డిక్షనరీ): పైథాన్ లో వాడే {key: value} ఫార్మాట్.
# మీరు model.state_dict() అని పిలవగానే, మోడల్ తన లోపల ఉన్న బ్రెయిన్ (నెట్‌వర్క్) మొత్తాన్ని ఒక డిక్షనరీ లాగా మార్చి బయటకు ఇస్తుంది.
# అది చూడటానికి ఇలా ఉంటుంది:
# code
# Python
# {
#   'Feature_net.conv1.weight': [0.5, 0.2, 0.1...],  # కన్వల్యూషన్ 1 లోని నంబర్లు
#   'Feature_net.conv1.bias': [0.01, 0.05...],
#   'Feature_net.conv2.weight': [0.9, 0.8...]        # కన్వల్యూషన్ 2 లోని నంబర్లు
# }
# అందుకే కోడ్‌లో model_dict = model.state_dict() అని రాశారు. (అంటే ప్రస్తుత ఖాళీ మోడల్ యొక్క ఆకారాన్ని ఒక వేరియబుల్ లో దాచుకున్నారు).
update_dict = {k: v for k, v in pretrained_w.items() if k in model_dict}
#మీరు డౌన్‌లోడ్ చేసుకున్న పాత బ్రెయిన్ (pretrained_w) ని నేరుగా కొత్త మోడల్ లోకి ఎక్కించేయొచ్చు కదా? ఈ లైన్ ఎందుకు రాశారు?
# కారణం: కొన్నిసార్లు పాత మోడల్‌లో 10 లేయర్స్ ఉండొచ్చు, కానీ మీ కొత్త మోడల్‌లో 8 లేయర్స్ మాత్రమే ఉండొచ్చు (లేదా పేర్లు మారి ఉండొచ్చు). అలాంటప్పుడు నేరుగా ఎక్కిస్తే Error వస్తుంది.
# ఈ లైన్ (దీన్ని Dictionary Comprehension అంటారు) ఏం చేస్తుందంటే:
# పాత మోడల్‌లోని పేర్లను (k), దాని వాల్యూస్‌ని (v) ఒక్కొక్కటిగా బయటకు తీస్తుంది.
# if k in model_dict : ఆ పేరు గనుక మన కొత్త మోడల్‌లో కూడా ఉంటేనే దాన్ని తీసుకుంటుంది.
# ఒకవేళ పాత మోడల్‌లో ఏదైనా ఎక్స్‌ట్రా లేయర్ ఉంటే దాన్ని వదిలేస్తుంది!
# అలా మన మోడల్‌కి సరిపోయే వాల్యూస్‌ని మాత్రమే ఫిల్టర్ చేసి update_dict లో పెట్టుకున్నారు. ఆ తర్వాత వాటిని సేఫ్ గా మోడల్ లోకి ఎక్కించారు!
assert len(update_dict) == len(model_dict), "Some weights are not loaded"
# అసలు ఏం జరుగుతోంది? ఇది ఒక "సెక్యూరిటీ చెక్ (Security Check)".
# మనం ఇందాక పాత మోడల్ నుండి మనకు కావాల్సిన లేయర్స్ మాత్రమే ఫిల్టర్ చేసి update_dict లో పెట్టుకున్నాం కదా? ఇప్పుడు ఈ లైన్ ఏం చెక్ చేస్తుందంటే:
# "మన కొత్త మోడల్‌కి ఎన్ని లేయర్స్ కావాలో (len(model_dict)), మనం పాత మోడల్ నుండి తెచ్చిన లేయర్స్ సంఖ్య (len(update_dict)) కూడా అంతే ఉందా లేదా?" అని చెక్ చేస్తుంది.
# ఒకవేళ రెండు సమానంగా ఉంటే: (అంటే ఉదాహరణకు రెండు డిక్షనరీల్లోనూ 50 లేయర్స్ ఉంటే), అప్పుడు కోడ్ ఎలాంటి ఆటంకం లేకుండా సైలెంట్‌గా ముందుకు వెళ్తుంది.
# ఒకవేళ సమానంగా లేకపోతే?: (అంటే పాత మోడల్‌లో 48 లేయర్స్ ఉండి, మీ కొత్త మోడల్‌కి 50 లేయర్స్ అవసరమైతే), assert అనే పైథాన్ కమాండ్ వెంటనే కోడ్‌ను అక్కడే ఆపేసి క్రాష్ చేస్తుంది (Exception వేస్తుంది). అప్పుడు మీకు స్క్రీన్ మీద "Some weights are not loaded" (కొన్ని బరువులు ఎక్కించబడలేదు) అని ఒక వార్నింగ్ మెసేజ్ కనిపిస్తుంది.
# ఎందుకు ఈ చెక్? ఒకవేళ ఏ ఒక్క లేయర్‌కి పాత బ్రెయిన్ అందకపోయినా.. ఆ లేయర్ గతి తప్పి పిచ్చి పిచ్చిగా ప్రవర్తిస్తుంది, మొత్తం మోడల్ ఔట్‌పుట్ పాడవుతుంది. అందుకే ఈ భద్రత!

model_dict.update(update_dict)
model.load_state_dict(model_dict)
# అసలు ఏం జరుగుతోంది? ఇక్కడ రెండు పైథాన్ డిక్షనరీలు (Dictionaries) ఉన్నాయి.
# model_dict: ఇది మన కొత్త మోడల్ యొక్క ఖాళీ బ్రెయిన్ (దీని నిండా ఏమీ తెలియని చెత్త నంబర్స్ ఉంటాయి).
# update_dict: ఇది మనం తెచ్చిన పాత బ్రెయిన్ (ఇందులో ఎంతో కష్టపడి నేర్చుకున్న కచ్చితమైన నంబర్స్ ఉంటాయి).
# పైథాన్‌లో .update() అనే ఫంక్షన్ వాడినప్పుడు, ఒక డిక్షనరీలోని వాల్యూస్ అన్నీ వెళ్లి, వేరొక డిక్షనరీలో అదే పేరుతో ఉన్న పాత వాల్యూస్‌ని రీప్లేస్ (Replace) చేస్తాయి (ఓవర్‌రైట్ చేస్తాయి).
# అంటే, ఖాళీగా ఉన్న కొత్త బ్రెయిన్ డిక్షనరీలోకి పాత బ్రెయిన్ వాల్యూస్ అన్నీ విజయవంతంగా ఎక్కించబడ్డాయి. కానీ... ఇవి ఇంకా డిక్షనరీ రూపంలోనే (ఒక వేరియబుల్ లోనే) ఉన్నాయి. ఇంకా మోడల్‌లోకి వెళ్ళలేదు!

